int	deja_present(int i, int grille[][4], int x, int y)
{
	int	j;

	j = 0;
	while (j <= 3)
	{
		if (grille[x][j] == i)
			return (0);
		j++;
	}
	j = 0;
	while (j <= 3)
	{
		if (grille[j][y] == i)
			return (0);
		j++;
	}
	return (1);
}
